import java.util.Scanner;

public class Loops {

    public static void main(String[] args) {

      //ForLoop
       for (int i = 0; i < 11; i++) {
           System.out.println(i);
       }

      //WhileLoop
       int i = 0;
       while (i < 11) {
           System.out.println(i);
           i++;
       }

       //DoWhile
       int j = 0;
       do {
           System.out.println(j);
           j++;
       } while (j < 11);

    }

}
